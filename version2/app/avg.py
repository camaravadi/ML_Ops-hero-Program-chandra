# Simple Python program to find the average of 10 numbers

# Hard-coded list of 10 numbers
numbers = [12, 45, 67, 23, 89, 34, 56, 78, 90, 11]

# Calculate the sum of the numbers
total = sum(numbers)

# Calculate the average
average = total / len(numbers)

# Print the result
print("My numbers are:", numbers)
print("My average is:", average)
print("The average value is not good")
